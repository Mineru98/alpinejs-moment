function build(options) {
    options.define || (options.define = {});
    options.define["process.env.NODE_ENV"] = process.argv.includes("--watch")
        ? `"production"`
        : `"development"`;

    return require("esbuild")
        .build({
            watch: process.argv.includes("--watch"),
            // external: ["alpinejs"],
            ...options,
        })
        .catch(() => process.exit(1));
}
